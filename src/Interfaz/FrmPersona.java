    private void jBAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAgregarActionPerformed
        //Primer paso, crear una persona.
        Persona persona = new Persona ();
        
        if(Persona.esNumero(jTCedula.getText())){
        persona.setCedula(Integer.parseInt(jTCedula.getText()));
        JOptionPane.showMessageDialog(null,"La persona se guardo con exito!","OJO",JOptionPane.INFORMATION_MESSAGE);
        }else {
        JOptionPane.showMessageDialog(null,"Error, No se pudo agregar la persona!","OJO", JOptionPane.ERROR_MESSAGE);
        return;
        }
        
        
        
        persona.setNombre(jTNombre.getText());
        persona.setApellidos(jTApellido.getText());
        persona.setAnoNac(Integer.parseInt(jTAno.getText()));
        persona.setEdad(Integer.parseInt(jTEdad.getText()));
        
        //Segundo paso, agregar la persona a la lista.
        P.add(persona);
        Persona.CargaTabla(P, jTable1);
        
        
        

    }//GEN-LAST:event_jBAgregarActionPerformed

